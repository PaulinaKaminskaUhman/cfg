<ul>
<li><b>INGREDIENTS:</b></li>
<li>125ml sunflower oil, plus extra for the tin</li>
<li>175g self-raising flour</li>
<li>175g caster sugar</li>
<li>125ml 10% fat Greek yogurt</li>
<li>3 eggs</li>
<li>1 tsp vanilla extract</li>
<li>1 lemon, zested</li>
<li>200g raspberries</li>
<li><b>For the decoration:</b></li>
<li>icing sugar, to dust</li>
<li>100g raspberries (optional)</li>

<li><b>INSTRUCTIONS:</b></li>
<li>STEP 1</li>
<li>Heat the oven to 180C/160C fan/ gas 4. Oil a deep, 20cm loose-bottomed cake tin and line with baking parchment. Combine the flour and sugar in a bowl. Set aside.</li>

<li>STEP 2</li>
<li>Mix the oil, yogurt, eggs, vanilla extract and lemon zest in another bowl. Tip in the flour and sugar mixture and fold in until smooth. Gently fold in the raspberries and bake for 30-40 mins, or until a skewer inserted into the middle comes out clean. Leave to cool in the tin for a few minutes, 
then cool completely on a wire rack. Dust lightly with icing sugar and serve topped with more raspberries, if you like. Will keep for up to three days in an airtight container.</li>

</ul>

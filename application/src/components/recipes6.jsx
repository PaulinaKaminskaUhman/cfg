<ul>
<li><b>INGREDIENTS:</b></li>
<li>1 1/2 cups all-purpose flour</li>
<li> 1/2 tsp baking powder</li>
<li>Tbsp lemon zest</li>
<li>1/2 tsp salt</li>
<li>1/2 cup unsalted butter, room temperature</li>
<li>1 cup granulated sugar</li>
<li>2 large eggs, room temperature</li>
<li>1 tsp vanilla extract</li>
<li>2 Tbsp lemon juice, about 1/2 lemon</li>
<li>1/2 cup buttermilk, see below for substitution</li>
<li>FOR THE LEMON SYRUP:</li>
<li>1/4 cup lemon juice, about 1 lemon</li>
<li>3 Tbsp powdered sugar</li>
<li>FOR THE LEMON ICING:</li>
<li>1 cup powdered sugar, sifted</li>
<li>1.5 Tbsp lemon juice</li>
<li>1 Tbsp milk</li>

<li><b>INSTRUCTIONS:</b></li>
<li>Preheat oven to 350°F (177°C). Grease a 9x5-inch (or 8x4-inch) loaf pan.</li>
<li>In a medium bowl combine the flour, baking powder, lemon zest, and salt. Set aside.</li>
<li>In the bowl of a stand mixer fitted with the paddle attachment (or using an electric mixer), cream the butter and sugar together at medium-high speed until pale and fluffy, about 4-6 minutes. Scrape the sides of the bowl as needed.</li>
<li>With the mixer running on low-speed, add the eggs one at a time, then beat in the vanilla extract and lemon juice. Beat on medium-high speed until combined.</li>
<li>Scrape the batter into the prepared loaf pan and bake for 45-55 minutes until the cake is golden brown and a toothpick comes out mostly clean with only a couple moist crumbs. Baking times vary, so keep an eye on yours.</li>
</ul>
